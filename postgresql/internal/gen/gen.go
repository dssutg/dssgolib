// Generate performance-oriented code. This generator's code is not
// optimized for simplicity's and readability's sake but the output is.
package main

import (
	"bytes"
	"fmt"
	"go/format"
	"io"
	"log"
	"maps"
	"os"
	"slices"
	"unicode"

	"github.com/dssutg/dssgolib/postgresql/internal"
)

func main() {
	genIsKeyword()
}

// genIsKeyword generates the [IsKeyword] function.
func genIsKeyword() {
	// Benchmarks show that this trie-based code is 3.63x faster than hash map
	// and 11.34x faster than binary search. This code is also a little faster
	// than the more compact (both its source code and amd64 assembly) version
	// of it where switch statements replaced with if or boolean expressions.

	var out bytes.Buffer

	// Group all keywords based on their length.
	wordsByLen := make(map[int][]string)
	for _, word := range internal.Keywords {
		wordsByLen[len(word)] = append(wordsByLen[len(word)], word)
	}

	// Generate the header.
	fmt.Fprintln(&out, `
		// Code generated by gen.go; DO NOT EDIT.
		package postgresql
		
		// IsKeyword reports whether the name is a PostgreSQL reserved keyword.
		func IsKeyword(name string) bool {
			switch len(name) {`)

	// Generate code for each word group based on their length.
	// Sort the lengths for deterministic output.
	lengths := slices.Collect(maps.Keys(wordsByLen))
	slices.Sort(lengths)
	for _, length := range lengths {
		// Generate search for a word group.
		slices.Sort(wordsByLen[length])
		fmt.Fprintf(&out, "case %d:\n", length)
		emitLevelSwitch(&out, 0, length, wordsByLen[length])
	}

	// Generate the footer.
	fmt.Fprintln(&out, "}")
	fmt.Fprintln(&out, "return false")
	fmt.Fprintln(&out, "}")

	// Format and print the generated code.
	formatted, err := format.Source(out.Bytes())
	if err != nil {
		log.Fatal(err)
	}

	if err := os.WriteFile("is_keyword_gen.go", formatted, 0o600); err != nil {
		log.Fatal(err)
	}
}

// emitLevelSwitch generates trie search code for a list of sorted words.
func emitLevelSwitch(w io.Writer, level, length int, words []string) {
	if level >= length {
		// Leaf of the tree.
		fmt.Fprintf(w, "return true // %s\n", words[0])
		return
	}

	// Generate the sub-node.
	fmt.Fprintf(w, "switch name[%d] {\n", level)

	// Get all words by the first character.
	wordsByChar := make(map[byte][]string)
	for _, word := range words {
		c := word[level]
		wordsByChar[c] = append(wordsByChar[c], word)
	}

	// Iterate over sorted list of unique first characters.
	firstChars := slices.Collect(maps.Keys(wordsByChar))
	slices.Sort(firstChars)
	for _, c := range firstChars {
		// Consider case-insensitivity.
		r := rune(c)
		lower := unicode.ToLower(r)
		upper := unicode.ToUpper(r)
		if lower != upper {
			fmt.Fprintf(w, "case %q, %q:\n", lower, upper)
		} else {
			fmt.Fprintf(w, "case %q:\n", lower)
		}
		// Emit sub-node search for the children of this character.
		emitLevelSwitch(w, level+1, length, wordsByChar[c])
	}

	// Terminate this sub-node.
	fmt.Fprintln(w, "}")
}
